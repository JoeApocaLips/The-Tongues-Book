def make_stem(prompt):
    return f"tongue_{abs(hash(prompt)):016x}"[:19]

def get_paths(stem):
    return (OUTPUT_DIR / f"{stem}.png",
            OUTPUT_DIR / f"{stem}.jpg",
            OUTPUT_DIR / f"{stem}.json")

# ============================================
# B&W — red/orange filter
# ============================================
def process_image(raw_bytes, jpg_path):
    img = Image.open(io.BytesIO(raw_bytes)).convert("RGB")
    w, h = img.size
    min_side = min(w, h)
    if min_side < MIN_WIDTH:
        scale = MIN_WIDTH / min_side
        img = img.resize((int(w * scale), int(h * scale)), Image.LANCZOS)
    arr  = np.array(img, dtype=np.float32)
    gray = np.clip(BW_R*arr[:,:,0] + BW_G*arr[:,:,1] + BW_B*arr[:,:,2], 0, 255)
    img  = ImageEnhance.Contrast(
               Image.fromarray(gray.astype(np.uint8), "L")
           ).enhance(CONTRAST_FACTOR)
    img  = img.point([int((i/255.0)**GAMMA * 255) for i in range(256)])
    img.save(jpg_path, "JPEG", dpi=(TARGET_DPI, TARGET_DPI), quality=85)

