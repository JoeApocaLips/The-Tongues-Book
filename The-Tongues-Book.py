def _compute_reject(data):
    """Compute reject from boolean criteria — don't trust model's own reject field."""
    return (
        not data.get("tongue_visible", False) or
        data.get("tongue_oversized", False) or
        not data.get("face_human", True) or
        not data.get("mouth_anatomy_ok", True)
    )

def _resize_png_b64(png_path, max_px=512):
    img = Image.open(png_path).convert("RGB")
    w, h = img.size
    if max(w, h) > max_px:
        scale = max_px / max(w, h)
        img = img.resize((int(w * scale), int(h * scale)), Image.LANCZOS)
    buf = io.BytesIO()
    img.save(buf, format="JPEG", quality=85)
    import base64
    return base64.b64encode(buf.getvalue()).decode()

def score_one(png_path, api_key):
    score_path = png_path.with_suffix(".score.json")
    if score_path.exists():
        return True, png_path.name, "skipped"
    try:
        b64  = _resize_png_b64(png_path)
        resp = requests.post(
            TEXT_BASE_URL,
            headers={"Authorization": f"Bearer {api_key}",
                     "Content-Type": "application/json"},
            json={
                "model": SCORE_MODEL,
                "messages": [{"role": "user", "content": [
                    {"type": "image_url",
                     "image_url": {"url": f"data:image/jpeg;base64,{b64}"}},
                    {"type": "text", "text": SCORE_PROMPT},
                ]}],
                "max_tokens": 200,
                "temperature": 0.1,
            },
            timeout=30,
        )
        resp.raise_for_status()
        data         = json.loads(resp.json()["choices"][0]["message"]["content"].strip())
        data["png"]  = png_path.name
        data["reject"] = _compute_reject(data)  # override model's judgment
        score_path.write_text(json.dumps(data, indent=2), encoding="utf-8")
        defects = ", ".join(data.get("defects", [])) or "none"
        return True, png_path.name, f"reject={data['reject']}  defects=[{defects}]"
    except Exception as e:
        return False, png_path.name, str(e)

