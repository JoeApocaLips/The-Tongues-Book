def make_contact():
    pngs = sorted(OUTPUT_DIR.glob("tongue_*.png"), key=lambda p: p.stat().st_mtime, reverse=True)
    if not pngs:
        print("No PNG found."); return
    if not CONTACT_TEMPLATE.exists():
        print(f"ERROR: template not found: {CONTACT_TEMPLATE}"); return
    already = {f.stem for f in PDF_DIR.glob("*.jpg")}
    # not yet selected first, already selected at end — both sorted by mtime desc
    pngs = [p for p in pngs if p.stem not in already] + \
           [p for p in pngs if p.stem in already]
    items = "\n".join(
        f'<div class="thumb{" kept" if p.stem in already else ""}" '
        f'data-stem="{p.stem}" data-kept="{str(p.stem in already).lower()}" onclick="toggle(this)">'
        f'<img src="{p.resolve().as_posix()}">'
        f'<div class="label">{p.stem[-8:]}</div></div>'
        for p in pngs
    )
    tpl = CONTACT_TEMPLATE.read_text(encoding="utf-8")
    html = (tpl
        .replace("%%ITEMS%%",  items)
        .replace("%%TOTAL%%",  str(len(pngs)))
        .replace("%%IN_PDF%%", str(len(already))))
    CONTACT_HTML.write_text(html, encoding="utf-8")
    print(f"Contact sheet: {CONTACT_HTML}  ({len(pngs)} images, {len(already)} already in for_pdf/)")

