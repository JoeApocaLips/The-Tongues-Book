def build_prompt(c):
    piercing   = (f", {c['tongue_piercing_type']} piercing on {c['tongue_piercing_location']}"
                  if c["tongue_piercing_type"] != "None" else "")
    device     = f", shot on {c['capture_device']}" if c["capture_device"] != "None" else ""
    state      = f", {c['tongue_state']}" if c.get("tongue_state", "normal") != "normal" else ""
    ethnicity  = f"{c['ethnicity']} " if c.get("ethnicity", "") else ""
    detail     = next((v for v in [
        c.get("tongue_accessory","None") != "None" and c.get("tongue_accessory"),
        c.get("prop","None")    != "None" and f"{c['prop']} visible",
        c.get("hand","None")    != "None" and c["hand"],
        c.get("surface","None") != "None" and f"tongue {c['surface']}",
    ] if v), "")
    return (
        f"{c['image_artifact']}, {c['photo_type']}, {c['background_style']}, "
        f"{c['lighting_style']}{device}, "
        f"{ethnicity}{c['gender']} sticking out their tongue, {c['tongue_pose']}{state}, "
        f"{c['tongue_expression']} expression, {c['framing']}"
        f"{piercing}{', ' + detail if detail else ''}"
    )

