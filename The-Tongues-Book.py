# ============================================
# BALANCED SAMPLER — random.choices
# ============================================
def balanced_sample(n, mode):
    param_keys = get_param_keys(mode)
    combos, attempts = [], 0
    while len(combos) < n and (attempts := attempts + 1) <= n * 100:
        c = {k: random.choices(v, weights=w, k=1)[0]
             for k, v, w in param_keys} | {"mode": mode}
        if is_valid_combo(c, mode):
            combos.append(c)
    if len(combos) < n:
        print(f"Warning: only {len(combos)}/{n} valid combos after {attempts} attempts")
    return combos

