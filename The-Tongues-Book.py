# TEXT GENERATOR
# ============================================
TEXT_MODEL     = "grok-4-1-fast-non-reasoning"
TEXT_BASE_URL  = "https://api.x.ai/v1/chat/completions"

def _load_system_prompt():
    return SYSTEM_PROMPT_PATH.read_text(encoding="utf-8").strip()

def _build_text_prompt(data):
    """Build a prompt from JSON params for text generation."""
    fields = [
        ("photo_type",        "photo type"),
        ("tongue_expression", "expression"),
        ("background_style",  "background"),
        ("capture_device",    "capture device"),
        ("tongue_state",      "tongue state"),
        ("prop",              "prop"),
        ("mode",              "mode"),
    ]
    skip = {"None", "normal"}
    parts = [f"{label}: {data[key]}"
             for key, label in fields
             if data.get(key, "") and data.get(key, "") not in skip]
    return "photograph parameters:\n" + "\n".join(parts)

def generate_text_one(jpg_path, api_key, system_prompt):
    """Generate a .txt caption for a jpg if not already present."""
    txt_path = jpg_path.with_suffix(".txt")
    if txt_path.exists():
        return True, jpg_path.name, "skipped"

    # JSON lives in tongues/, not for_pdf/
    json_path = jpg_path.with_suffix(".json")
    if not json_path.exists():
        json_path = OUTPUT_DIR / (jpg_path.stem + ".json")
    if not json_path.exists():
        return False, jpg_path.name, "no JSON found"

    try:
        data   = json.loads(json_path.read_text(encoding="utf-8"))
        prompt = _build_text_prompt(data)

        resp = requests.post(
            TEXT_BASE_URL,
            headers={"Authorization": f"Bearer {api_key}",
                     "Content-Type": "application/json"},
            json={
                "model": TEXT_MODEL,
                "messages": [
                    {"role": "system", "content": system_prompt},
                    {"role": "user",   "content": prompt},
                ],
                "max_tokens": 60,
                "temperature": 0.85,
                "top_p": 0.92,
            },
            timeout=30,
        )
        resp.raise_for_status()
        text = resp.json()["choices"][0]["message"]["content"].strip()
        txt_path.write_text(text, encoding="utf-8")
        return True, jpg_path.name, text

    except Exception as e:
        return False, jpg_path.name, str(e)

def generate_texts():
    api_key       = os.environ.get(API_KEY_ENV) or sys.exit(f"Error: {API_KEY_ENV} not set")
    system_prompt = _load_system_prompt()
    jpgs          = sorted(PDF_DIR.glob("*.jpg"))
    todo          = [j for j in jpgs if not j.with_suffix(".txt").exists()]
    if not todo:
        print("All captions already generated."); return

    print(f"Generating captions for {len(todo)}/{len(jpgs)} images  "
          f"[model: {TEXT_MODEL}, workers: {TEXT_WORKERS}]")

    CAPTIONS_LOG_DIR.mkdir(parents=True, exist_ok=True)
    log_path = CAPTIONS_LOG_DIR / f"captions_{datetime.now().strftime('%Y%m%d_%H%M%S')}.log"
    log_lines = [
        f"captions log — {datetime.now().isoformat()}",
        f"model: {TEXT_MODEL}  workers: {TEXT_WORKERS}",
        f"prompt: {SYSTEM_PROMPT_PATH}",
        "-" * 60,
    ]

    success = failed = skipped = 0
    with ThreadPoolExecutor(max_workers=TEXT_WORKERS) as ex:
        futures = {ex.submit(generate_text_one, j, api_key, system_prompt): j for j in todo}
        for f in as_completed(futures):
            ok, name, info = f.result()
            if info == "skipped":
                skipped += 1
            elif ok:
                success += 1
                line = f"OK   {name}  |  {info}"
                print(line)
                log_lines.append(line)
            else:
                failed += 1
                line = f"ERR  {name}  |  {info}"
                print(line)
                log_lines.append(line)

    summary = f"\nDone: {success} generated, {skipped} skipped, {failed} failed"
    print(summary)
    log_lines.append("-" * 60)
    log_lines.append(summary)
    log_path.write_text("\n".join(log_lines), encoding="utf-8")
    print(f"Log: {log_path}")


