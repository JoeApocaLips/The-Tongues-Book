REVIEW_FILE = PDF_DIR / "bad_captions_review.txt"

def _write_review(lines, header_note):
    header = [
        "# bad_captions_review.txt",
        f"# {header_note}",
        "# Lines starting with '-' will be DELETED. Remove '-' to keep.",
        "# Then run: del_bad_captions.bat",
        "",
    ]
    REVIEW_FILE.write_text("\n".join(header + lines), encoding="utf-8")

def find_bad():
    import re
    patterns = _load_bad_patterns()
    rx   = re.compile("|".join(patterns), re.IGNORECASE)
    txts = sorted(PDF_DIR.glob("*.txt"))
    seen = set()
    found = []
    for t in txts:
        if t.name == REVIEW_FILE.name: continue
        if not t.with_suffix(".jpg").exists(): continue
        if rx.search(text := t.read_text(encoding="utf-8")) and t.name not in seen:
            seen.add(t.name)
            found.append((t, text.strip()))
    if not found:
        print("No bad patterns found."); return
    lines = [f"- {t.name}  |  {text}" for t, text in found]
    _write_review(lines, "Suspected bad captions — remove '-' to keep")
    print(f"Found {len(found)} suspect caption(s) — opening review file...")

def all_captions():
    txts = sorted(PDF_DIR.glob("*.txt"))
    seen = set()
    lines = []
    for t in txts:
        if t.name == REVIEW_FILE.name: continue
        if not t.with_suffix(".jpg").exists(): continue  # skip orphan txt
        if t.name in seen: continue
        seen.add(t.name)
        lines.append(f"{t.name}  |  {t.read_text(encoding='utf-8').strip()}")
    if not lines:
        print("No captions found."); return
    _write_review(lines, "All captions — add '-' at start of lines to DELETE")
    print(f"{len(lines)} captions — opening review file...")

def clean_captions():
    if not REVIEW_FILE.exists():
        print("No review file found — run --find-bad or --all-captions first"); return
    lines = [l for l in REVIEW_FILE.read_text(encoding="utf-8").splitlines()
             if l.strip() and not l.startswith("#")]
    seen = set()
    deleted = 0
    for line in lines:
        if not line.startswith("-"): continue   # keep lines without -
        stem = line[1:].split("|")[0].strip()
        if not stem or stem in seen or stem == REVIEW_FILE.name: continue
        seen.add(stem)
        txt = PDF_DIR / stem
        if txt.exists():
            txt.unlink()
            print(f"DEL {stem}")
            deleted += 1
        else:
            print(f"NOT FOUND {stem}")
    if REVIEW_FILE.exists():
        REVIEW_FILE.unlink()
    print(f"\nDeleted {deleted} caption(s). Run --captions to regenerate.")

