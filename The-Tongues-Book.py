PLATE_BEGIN      = "%%BEGIN_PLATE%%"
PLATE_END        = "%%END_PLATE%%"
WHITE_BORDER_THR = 220   # pixel brightness threshold for white border detection
WHITE_BORDER_PX  = 20    # border band width in pixels

def _escape_tex(text):
    """Escape special LaTeX characters in caption text."""
    for char, repl in [("\\", r"\textbackslash{}"),
                        ("&", r"\&"), ("%", r"\%"), ("$", r"\$"),
                        ("#", r"\#"), ("_", r"\_"), ("{", r"\{"),
                        ("}", r"\}"), ("~", r"\textasciitilde{}"),
                        ("^", r"\^{}")]:
        text = text.replace(char, repl)
    return text

def _detect_style(jpg_path):
    """Detect plate style: platedefault | plateblack | platerotated | platerotatedblack."""
    import numpy as np
    arr  = np.array(Image.open(jpg_path).convert("L"))
    h, w = arr.shape
    rotated  = w > h
    top   = arr[:WHITE_BORDER_PX, :].mean()
    left  = arr[:, :WHITE_BORDER_PX].mean()
    right = arr[:, -WHITE_BORDER_PX:].mean()
    black_bg = all(v > WHITE_BORDER_THR for v in ([top, left] if rotated else [top, left, right]))
    return ("platerotatedblack" if rotated and black_bg else
            "platerotated"      if rotated else
            "plateblack"        if black_bg else
            "platedefault")

def make_pdf():
    if not TEX_TEMPLATE_PATH.exists():
        print(f"ERROR: template not found: {TEX_TEMPLATE_PATH}"); return
    jpgs = sorted(PDF_DIR.glob("*.jpg"))
    if not jpgs:
        print(f"No JPG found in {PDF_DIR}"); return

    template = TEX_TEMPLATE_PATH.read_text(encoding="utf-8")
    i_begin  = template.find(PLATE_BEGIN)
    i_end    = template.find(PLATE_END)
    if i_begin == -1 or i_end == -1:
        print(f"ERROR: {PLATE_BEGIN} / {PLATE_END} markers not found in template"); return

    plate_tpl = template[i_begin + len(PLATE_BEGIN):i_end]

    plates = []
    txt_count = style_counts = {"platedefault":0,"plateblack":0,"platerotated":0,"platerotatedblack":0}
    for i, jpg in enumerate(jpgs, 1):
        imgpath  = jpg.resolve().as_posix()
        txt_path = jpg.with_suffix(".txt")
        caption  = _escape_tex(txt_path.read_text(encoding="utf-8").strip()) if txt_path.exists() else f"tongue \\#{i:03d}"
        if txt_path.exists(): txt_count["platedefault"] += 0  # reuse dict as txt counter below
        style    = _detect_style(jpg)
        style_counts[style] = style_counts.get(style, 0) + 1
        plates.append(
            plate_tpl
            .replace("%%STYLE%%",   style)
            .replace("%%IMGPATH%%", imgpath)
            .replace("%%CAPTION%%", caption)
        )

    block = template[i_begin:i_end + len(PLATE_END)]
    tex   = template.replace(block, "\n".join(plates))
    TEX_OUT.write_text(tex, encoding="utf-8")
    captions_found = sum(1 for j in jpgs if j.with_suffix(".txt").exists())
    print(f"Written: {TEX_OUT}  ({len(jpgs)} plates, {captions_found} captions)")
    print(f"Styles: { {k:v for k,v in style_counts.items() if v} }")

