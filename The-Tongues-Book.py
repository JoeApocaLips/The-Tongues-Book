_POSES_PORTRAIT_ONLY       = {"tongue pushed into cheek", "tongue pressed against teeth",
                               "tongue touching upper lip", "tongue touching upper lip from inside"}
_SELFIE_DEVICES            = {"instax instant camera", "photo booth frame"}
_SELFIE_TYPES              = {"bathroom mirror selfie", "smartphone selfie", "polaroid instant photo"}
_DEEP_INCOMPATIBLE_DEVICES = {"VHS freeze frame", "CCTV still frame", "security camera footage"}
_MUGSHOT_TYPES             = {"mugshot / police lineup"}
_MUGSHOT_BACKGROUNDS       = {"neutral white wall", "wall with some graffiti",
                               "police lineup height chart wall"}
_BATHROOM_TYPES            = {"bathroom mirror selfie"}
_BATHROOM_BACKGROUNDS      = {"tiled bathroom wall, sink visible", "tiled bathroom wall, shower visible"}
_ANALOG_TYPES              = {"polaroid instant photo", "disposable camera snapshot"}
_DIGITAL_ARTIFACTS         = {"severe jpeg compression, blocky artifacts",
                               "low quality 240p screenshot",
                               "VHS tracking error, scan lines",
                               "accidental shot, tilted frame"}

def is_valid_combo(c, mode):
    framing, ptype, lighting, device, pose, bg = (
        c["framing"], c["photo_type"], c["lighting_style"],
        c["capture_device"], c["tongue_pose"], c["background_style"]
    )
    if pose in _POSES_PORTRAIT_ONLY and framing in (framings_tongue + framings_deep):
        return False
    if lighting == "harsh phone flash" and framing in (framings_tongue + framings_deep):
        return False
    if lighting == "golden hour / sunset glow" and ptype in {
            "halftone newspaper reproduction", "webcam capture"}:
        return False
    if framing in framings_deep and ptype in _SELFIE_TYPES:
        return False
    if device == "endoscope camera" and framing not in framings_deep:
        return False
    if device in _SELFIE_DEVICES and ptype in _SELFIE_TYPES:
        return False
    if device in _DEEP_INCOMPATIBLE_DEVICES and framing in framings_deep:
        return False
    # bathroom mirror selfie must be in bathroom bg
    if ptype in _BATHROOM_TYPES and bg not in _BATHROOM_BACKGROUNDS:
        return False
    # bathroom mirror selfie incompatible with VHS/CCTV/security
    if ptype in _BATHROOM_TYPES and device in _DEEP_INCOMPATIBLE_DEVICES:
        return False
    # mugshot: portrait only, restricted backgrounds
    if ptype in _MUGSHOT_TYPES:
        if framing in (framings_tongue + framings_deep):
            return False
        if bg not in _MUGSHOT_BACKGROUNDS:
            return False
    # strict side profile: not with tongue/deep framings
    if framing == "strict side profile" and framing in (framings_tongue + framings_deep):
        return False
    # outdoor park / urban street art: not with deep framings
    if bg in {"outdoor park, trees and sunlight", "urban street art mural"} and framing in framings_deep:
        return False
    # height chart wall: mugshot only
    if bg == "police lineup height chart wall" and ptype not in _MUGSHOT_TYPES:
        return False
    # car interior: not deep framings
    if bg == "car interior, seat belt visible" and framing in framings_deep:
        return False
    # bed/pillow: overhead angle or selfie only
    if bg == "unmade bed, pillow visible" and framing not in {
            "overhead angle, subject lying back", "selfie", "close-up portrait"}:
        return False
    # indoor party dark: not with mugshot
    if bg == "crowded indoor party, dark room" and ptype in _MUGSHOT_TYPES:
        return False
    # analog photo types: no digital artifacts
    if ptype in _ANALOG_TYPES and c.get("image_artifact", "") in _DIGITAL_ARTIFACTS:
        return False
    return True

