# REPROCESS
# ============================================
def _reprocess_one(png):
    try:
        process_image(png.read_bytes(), png.with_suffix(".jpg"))
        return True, png.name
    except Exception as e:
        return False, f"{png.name}: {e}"

def reprocess_missing():
    if not (todo := [p for p in sorted(OUTPUT_DIR.glob("tongue_*.png"))
                     if not p.with_suffix(".jpg").exists()]):
        print("Nothing to reprocess."); return
    print(f"Reprocessing {len(todo)} PNG(s) [{REPROCESS_WORKERS} workers]...")
    ok = fail = 0
    with ThreadPoolExecutor(max_workers=REPROCESS_WORKERS) as ex:
        for success, msg in ex.map(_reprocess_one, todo):
            print(f"{'OK ' if success else 'ERR'} {msg}")
            if success: ok += 1
            else:       fail += 1
    print(f"Done: {ok} OK, {fail} failed")

# ============================================
# SINGLE IMAGE PIPELINE
# ============================================
def generate_one(combo, api_key, thread_idx, forced_stem=None):
    time.sleep(thread_idx * THREAD_START_DELAY)
    prompt = build_prompt(combo)
    stem   = forced_stem or make_stem(prompt)
    if forced_stem and (OUTPUT_DIR / f"{stem}.png").exists():
        stem = make_stem(prompt + str(random.random()))
    raw_path, jpg_path, json_path = get_paths(stem)

    for attempt in range(1, RETRY_ATTEMPTS + 1):
        try:
            resp = requests.post(
                f"{BASE_URL}/images/generations",
                headers={"Authorization": f"Bearer {api_key}",
                         "Content-Type": "application/json"},
                json={"model": IMAGE_MODEL, "prompt": prompt,
                      "n": 1, "response_format": "url"},
                timeout=120,
            )
            resp.raise_for_status()
            if not (img_r := requests.get(resp.json()["data"][0]["url"], timeout=60)).ok:
                img_r.raise_for_status()
            raw_path.write_bytes(raw_bytes := img_r.content)
            process_image(raw_bytes, jpg_path)
            json_path.write_text(
                json.dumps(dict(combo) | {"prompt": prompt}, ensure_ascii=False, indent=2),
                encoding="utf-8"
            )
            print(f"OK  {raw_path.name}  {jpg_path.name}  {json_path.name}")
            return True
        except Exception as e:
            print(f"ERR attempt {attempt}/{RETRY_ATTEMPTS}: {e}")
            if attempt < RETRY_ATTEMPTS:
                time.sleep(RETRY_DELAY ** attempt)

    print(f"FAIL {stem}")
    return False

